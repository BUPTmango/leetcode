package middle_level;

/**
 * 904. 水果成篮
 * 在一排树中，第 i 棵树产生tree[i] 型的水果。
 * 你可以从你选择的任何树开始，然后重复执行以下步骤：
 * <p>
 * 把这棵树上的水果放进你的篮子里。如果你做不到，就停下来。
 * 移动到当前树右侧的下一棵树。如果右边没有树，就停下来。
 * 请注意，在选择一颗树后，你没有任何选择：你必须执行步骤 1，然后执行步骤 2，然后返回步骤 1，然后执行步骤 2，依此类推，直至停止。
 * <p>
 * 你有两个篮子，每个篮子可以携带任何数量的水果，但你希望每个篮子只携带一种类型的水果。
 * <p>
 * 用这个程序你能收集的水果树的最大总量是多少？
 * <p>
 * 示例 1：
 * <p>
 * 输入：[1,2,1]
 * 输出：3
 * 解释：我们可以收集 [1,2,1]。
 * 示例 2：
 * <p>
 * 输入：[0,1,2,2]
 * 输出：3
 * 解释：我们可以收集 [1,2,2]
 * 如果我们从第一棵树开始，我们将只能收集到 [0, 1]。
 * 示例 3：
 * <p>
 * 输入：[1,2,3,2,2]
 * 输出：4
 * 解释：我们可以收集 [2,3,2,2]
 * 如果我们从第一棵树开始，我们将只能收集到 [1, 2]。
 * 示例 4：
 * <p>
 * 输入：[3,3,3,1,2,1,1,2,3,3,4]
 * 输出：5
 * 解释：我们可以收集 [1,2,1,1,2]
 * 如果我们从第一棵树或第八棵树开始，我们将只能收集到 4 棵水果树。
 * <p>
 * <p>
 * 提示：
 * <p>
 * 1 <= tree.length <= 40000
 * 0 <= tree[i] < tree.length
 *
 * @author Wang Guolong
 * @version 1.0
 * @date 2020/10/25 10:20 上午
 */
public class TotalFruit {
    public int totalFruit(int[] tree) {
        int res = 0, len = tree.length;
        // 2种树的状态(one, two)， one初始化为tree数组的第1个元素
        int one = tree[0], two, begin = 0, end = 1;
        // 寻找two的初始值，以构成初始(one, two)
        while (end < len && tree[end] == one) {
            ++end;
        }
        // 若整个数组的元素都由初始的(one, two)所构成，则直接返回数组长度
        if (end == len) {
            return len;
        }
        // 构成初始的(one, two)
        two = tree[end++];
        for (; end < len; ++end) {
            // 遇到了第3种树
            if (one != tree[end] && two != tree[end]) {
                // 更新最终返回结果
                res = Math.max(res, end - begin);
                // (one, two)更新为(第3种树的左边的树, 第3种树)
                one = tree[end - 1];
                two = tree[end];
                // 更新由当前(one, two)所构成的连续子数组的左边界
                begin = end - 1;
                // 向左寻找由one构成的连续子数组
                while (tree[begin - 1] == one) {
                    --begin;
                }
            }
        }
        return Math.max(res, end - begin);
    }
}
